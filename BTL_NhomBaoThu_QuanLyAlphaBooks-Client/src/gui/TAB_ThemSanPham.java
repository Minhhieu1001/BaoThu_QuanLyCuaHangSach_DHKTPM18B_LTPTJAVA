    public SanPham getSanPham(SanPham currentSP) {
        boolean isEditMode = (currentSP != null);

        // Khai báo biến
        int temp = -1;
        String tempStr = "";

        // Lấy dữ liệu từ giao diện
        String tenSanPham = jTextField_TenSanPham.getText().trim();
        int soLuongTon;
        try {
            soLuongTon = Integer.parseInt(jTextField_SoLuongTon.getText().trim());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Số lượng tồn phải là số nguyên!");
            return null;
        }
        String barcode = jTextField_Barcode.getText().trim();
        double giaNhap;
        try {
            giaNhap = Double.parseDouble(jTextField_GiaNhap.getText().trim());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Giá nhập phải là số!");
            return null;
        }
        double thue;
        try {
            thue = Double.parseDouble(jTextField_Thue.getText().trim());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Thuế phải là số!");
            return null;
        }
        int namSanXuat;
        try {
            namSanXuat = Integer.parseInt(jTextField1_NamSanXuat.getText().trim());
        } catch (Exception e) {
            namSanXuat = 1;
        }
        String donViDoLuong = jTextField_DonViDoLuong.getText().trim();
        String kichThuoc = jTextField_KichThuoc.getText().trim();
        String xuatXu = jTextField_XuatXu.getText().trim();
        String ngonNgu = jTextField_NgonNgu.getText().trim();
        int soTrang;
        try {
            soTrang = Integer.parseInt(jTextField_SoTrang.getText().trim());
        } catch (Exception e) {
            soTrang = 1;
        }
        String loaiBia = jTextField_LoaiBia.getText().trim();
        String loaiDoiTra = jComboBox_LoaiDoiTra.getSelectedItem().toString();

        // Xử lý ngày nhập
        SimpleDateFormat dcn = new SimpleDateFormat("yyyy-MM-dd");
        String date;
        try {
            date = dcn.format(txtNgay.getDate());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Ngày nhập không hợp lệ!");
            return null;
        }
        Date DATE = Date.valueOf(date);

        // Khởi tạo các đối tượng liên quan
        NhaCungCap ncc = new NhaCungCap();
        TheLoai tl = new TheLoai();
        NhaXuatBan nxb = new NhaXuatBan();
        DanhMuc dm = new DanhMuc();
        TacGia tg = new TacGia();
        ThuongHieu th = new ThuongHieu();

        // Lấy thông tin từ giao diện
        String tenTacGia = jTextField_TacGia.getText().trim();
        String tenNhaCungCap = jTextField_NhaCungCap.getText().trim();
        String tenTheLoai = jTextField_TheLoai.getText().trim();
        String tenNhaXuatBan = jTextField_NhaXuatBan.getText().trim();
        String tenDanhMuc = jTextField_DanhMuc.getText().trim();

        try {
            if (tenNhaCungCap.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Tên nhà cung cấp không được rỗng!");
                return null;
            }
            ncc.setTenNhaCungCap(tenNhaCungCap);

            if (tenTheLoai.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Tên thể loại không được rỗng!");
                return null;
            }
            tl.setTenTheLoai(tenTheLoai);

            if (tenNhaXuatBan.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Tên nhà xuất bản không được rỗng!");
                return null;
            }
            nxb.setTenNhaXuatBan(tenNhaXuatBan);

            if (tenDanhMuc.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Tên danh mục không được rỗng!");
                return null;
            }
            dm.setTenDanhMuc(tenDanhMuc);

            if (tenTacGia.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Tên tác giả không được rỗng!");
                return null;
            }
            tg.setTenTacGia(tenTacGia);
        } catch (IllegalArgumentException e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
            return null;
        } catch (Exception e) {
            e.printStackTrace();
            return null;
        }

        // Kiểm tra và lấy ID từ cơ sở dữ liệu
        SanPham_BUS sanPham_BUS = new SanPham_BUS();

        // Nhà cung cấp
        tempStr = sanPham_BUS.getIdNhaCungCapByName(ncc.getTenNhaCungCap());
        if (tempStr.equals("")) {
            JOptionPane.showMessageDialog(null, "Nhà cung cấp này chưa có sẵn!");
            return null;
        } else {
            ncc.setNhaCungCapID(tempStr);
        }

        // Thể loại
        temp = sanPham_BUS.getIdTheloaiByName(tl.getTenTheLoai());
        if (temp == -1) {
            int check = JOptionPane.showConfirmDialog(null, "Thể loại này chưa có sẵn. Tạo mới?");
            if (check == 0) {
                TheLoai_BUS theLoai_BUS = new TheLoai_BUS();
                theLoai_BUS.addTheLoai(tl);
                tl.setTheLoaiID(sanPham_BUS.getIdTheloaiByName(tl.getTenTheLoai()));
            } else {
                return null;
            }
        } else {
            tl.setTheLoaiID(temp);
        }

        // Nhà xuất bản
        temp = sanPham_BUS.getIdNhaXuatBanByName(tenNhaXuatBan);
        if (temp == -1) {
            int check = JOptionPane.showConfirmDialog(null, "Nhà xuất bản '" + tenNhaXuatBan + "' chưa có sẵn. Tạo mới?");
            if (check == 0) {
                TAB_ThemNhaXuatBan themNhaSanXuat = new TAB_ThemNhaXuatBan(nxb);
                themNhaSanXuat.setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);
                themNhaSanXuat.setLocationRelativeTo(null);
                themNhaSanXuat.setVisible(true);
                return null;
            } else {
                return null;
            }
        } else {
            nxb.setNhaXuatBanID(temp);
        }

        // Danh mục
        temp = sanPham_BUS.getIdDanhMucByName(dm.getTenDanhMuc());
        if (temp == -1) {
            int check = JOptionPane.showConfirmDialog(null, "Danh mục này chưa có sẵn. Tạo mới?");
            if (check == 0) {
                DanhMuc_BUS danhMuc_BUS = new DanhMuc_BUS();
                danhMuc_BUS.addDanhMuc(dm);
                dm.setDanhMucID(sanPham_BUS.getIdDanhMucByName(dm.getTenDanhMuc()));
            } else {
                return null;
            }
        } else {
            dm.setDanhMucID(temp);
        }

        // Tác giả
        temp = sanPham_BUS.getIdTacGiaByName(tg.getTenTacGia());
        if (temp == -1) {
            int check = JOptionPane.showConfirmDialog(null, "Tác giả này chưa có sẵn. Tạo mới?");
            if (check == 0) {
                TacGia_BUS tacGia_BUS = new TacGia_BUS();
                tacGia_BUS.addTacGia(tg);
                tg.setTacGiaID(sanPham_BUS.getIdTacGiaByName(tg.getTenTacGia()));
            } else {
                return null;
            }
        } else {
            tg.setTacGiaID(temp);
        }

        // Thương hiệu
        if (isEditMode) {
            // Chế độ chỉnh sửa: Lấy thông tin thương hiệu từ sản phẩm hiện tại
            th.setThuongHieuID(currentSP.getThuongHieu().getThuongHieuID());
            try {
                th.setTenThuongHieu(currentSP.getThuongHieu().getTenThuongHieu());
            } catch (Exception e) {
                throw new RuntimeException(e);
            }
        } else {
            // Chế độ thêm mới: Gán mặc định thuongHieuID = 1
            th.setThuongHieuID(1);
            // Lấy tên thương hiệu từ cơ sở dữ liệu để đảm bảo tính nhất quán
            String tenThuongHieu = sanPham_BUS.getNameThuongHieuByID(1);
            if (tenThuongHieu == null || tenThuongHieu.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Thương hiệu với ID = 1 không tồn tại trong cơ sở dữ liệu!");
                return null;
            }
            try {
                th.setTenThuongHieu(tenThuongHieu);
            } catch (Exception e) {
                throw new RuntimeException(e);
            }
        }

        // Tạo đối tượng SanPham
        SanPham sanPham = new SanPham();
        try {
            sanPham.setTenSanPham(tenSanPham);
            sanPham.setSoLuongTon(soLuongTon);
            sanPham.setBarcode(barcode);
            sanPham.setGiaNhap(giaNhap);
            sanPham.setThue(thue);
            sanPham.setNamSanXuat(namSanXuat);
            sanPham.setDonViDoLuong(donViDoLuong);
            sanPham.setKichThuoc(kichThuoc);
            sanPham.setXuatXu(xuatXu);
            sanPham.setNgonNgu(ngonNgu);
            sanPham.setSoTrang(soTrang);
            sanPham.setLoaiBia(loaiBia);
            sanPham.setLoaiDoiTra(loaiDoiTra.equalsIgnoreCase("Được đổi trả") ? "DUOC_DOI_TRA" : "KHONG_DOI_TRA");
            sanPham.setNgayNhap(DATE);

            // Xử lý hình ảnh
            String partialPath = "";
            try {
                if (this.file != null && this.file.exists()) {
                    String filePath = this.file.getPath();
                    partialPath = filePath.substring(filePath.indexOf("img\\products"));
                    partialPath = partialPath.replace("\\", "/");
                    sanPham.setImgPath(partialPath);
                } else if (isEditMode) {
                    sanPham.setImgPath(currentSP.getImgPath());
                }
            } catch (Exception e) {
                if (isEditMode) {
                    sanPham.setImgPath(currentSP.getImgPath());
                }
            }

            // Xử lý các thuộc tính chỉ dùng khi chỉnh sửa
            if (isEditMode) {
                sanPham.setLoaiSanPham(currentSP.getLoaiSanPham());
                sanPham.setSanPhamID(currentSP.getSanPhamID());
            } else {
                // Thiết lập giá trị mặc định cho sản phẩm mới
                sanPham.setLoaiSanPham("SACH");
                sanPham.setTinhTrang("CON_HANG");
            }

            // Gán các đối tượng liên quan
            sanPham.setTacGia(tg);
            sanPham.setTheLoai(tl);
            sanPham.setNhaCungCap(ncc);
            sanPham.setDanhMuc(dm);
            sanPham.setNhaXuatBan(nxb);
            sanPham.setThuongHieu(th);
        } catch (Exception ex) {
            ex.printStackTrace();
            return null;
        }

        return sanPham;
    }

