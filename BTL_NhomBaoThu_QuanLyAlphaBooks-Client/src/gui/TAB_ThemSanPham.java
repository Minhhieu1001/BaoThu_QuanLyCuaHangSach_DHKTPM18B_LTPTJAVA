    private void jButton_ChonActionPerformed(ActionEvent evt) {//GEN-FIRST:event_jButton_ChonActionPerformed
        // TODO add your handling code here:
        JFileChooser chooser;
        BufferedImage img = null;
        chooser = new JFileChooser();
        chooser.showOpenDialog(null);
        file = chooser.getSelectedFile();
        if (file == null)
            return;
        try {
            FileInputStream fis = new FileInputStream(file);

            img = ImageIO.read(file);
            file = new File("src/img/products/" + file.getName());
            FileOutputStream fos = new FileOutputStream(new File("src/img/products/" + file.getName()));
            int length;
            byte[] buffer = new byte[1024];

            // copy the file content in bytes
            while ((length = fis.read(buffer)) > 0) {
                fos.write(buffer, 0, length);
            }

            fis.close();
            fos.close();
        } catch (Exception e) {
            e.printStackTrace();
        }

        Image newimg = img.getScaledInstance(140, 170, Image.SCALE_SMOOTH); // scale it the smooth way
        System.out.println(newimg.toString());
        ImageIcon icon = new ImageIcon(newimg);
        jLabel_ImgChooser.setIcon(icon);

    }

